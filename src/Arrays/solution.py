# An array is a data structure that stores a collection of elements, typically of the same data type, in contiguous memory locations

nums = [1, 2, 3, 4, 5] # This is an array of integers
fruits = ["apple", "banana", "cherry"] # This is an array of strings

# In Python, arrays can be implemented using lists, which are dynamic and can hold elements of different data types.

print("First number:", nums[0]) # Accessing the first element of the nums array
print("Second fruit:", fruits[-1]) # Accessing the last element of the fruits array